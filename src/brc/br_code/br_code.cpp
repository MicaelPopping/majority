#include "brc/br_code/br_code.hpp"


#include <cstdlib>


/************************************************************************
*                           Publics methods                             *
************************************************************************/
BR_Code::BR_Code(uint8_t inputs_number, uint8_t vector_size) 
: INPUTS_NUMBER(inputs_number) {

    if((br_code = (uint64_t*) malloc(vector_size * sizeof(uint64_t))) == NULL)
        br_code = nullptr;
}

/************************************************************************/

BR_Code::~BR_Code() {

    if(br_code == nullptr)
        return;

    free(br_code);
    br_code = nullptr;
}

